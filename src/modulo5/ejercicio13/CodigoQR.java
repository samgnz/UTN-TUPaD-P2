public class CodigoQR {
    private int valor;
    private Usuario usuario;

    public CodigoQR(int valor, Usuario usuario) {
        this.valor = valor;
        this.usuario = usuario;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }

    public Usuario getUsuario() {
        return usuario;
    }

    public void setUsuario(Usuario usuario) {
        this.usuario = usuario;
    }
    
    
}
